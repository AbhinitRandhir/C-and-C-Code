public class data
{

    static byte a;
    static short b;
    static int c;
    static float d;
    static double e;
    static long f;
    static char g;
    static boolean h;
    public static void main(String[] args)
     {
        System.out.println("The default values of primitive data types are:");
        System.out.println("Default value of bytes:"+a);
        System.out.println("Default value of short:"+b);
        System.out.println("Default value of int:"+c);
        System.out.println("Default value of float:"+d);
        System.out.println("Default value of double:"+e);
        System.out.println("Default value of long:"+f);
        System.out.println("Default value of char:"+g);
        System.out.println("Default value of boolean:"+h);

    }

}